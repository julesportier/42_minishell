#include "../minishell.h"

int	count_array_len(char **array)
{
	int	i;

	i = 0;
	if (array == NULL)
		return (0);
	while (array[i])
		i++;
	return (i);
}

char	*free_strjoin(char *s1, char *s2, t_bool free_s1, t_bool free_s2)
{
	char	*joined_str;

	joined_str = ft_strjoin(s1, s2);
	if (free_s1)
		free(s1);
	if (free_s2)
		free(s2);
	return (joined_str);
}
